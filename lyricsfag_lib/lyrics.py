LOG = logging.getLogger(__name__)

LRCLIB_BASE = "https://lrclib.net/api"
LRCLIB_TIMEOUT = 15
LRCLIB_USER_AGENT = "LyricsFAG/0.1 (https://github.com/local/lyricsfag)"

# Genius API access token (free at https://genius.com/api-clients).  When
# omitted, the library silently skips Genius and reports "no token".
ENV_GENIUS_TOKEN = "GENIUS_ACCESS_TOKEN"

# Human-readable marker prepended to ``diagnostic_lines`` whenever the
# lyrics came from local ``faster-whisper`` transcription.  The square-
# bracket-hash form matches the existing ``[# ...]`` line convention so
# :func:`split_synced_text` and :func:`header_lines` skip it (they
# neither match the ``[mm:ss]`` timestamp gate nor the
# ``[a-zA-Z]+:..]`` ID3 header regex).  See
# :func:`_format_diagnostics` for the parser-safety contract.
WHISPER_PROVIDER_MARKER = "[# Generated Lyrics with Whisper]"
# Variant emitted when Whisper ran on vocals pre-isolated by Demucs; the
# .lrc reader sees ONE human-readable line, so we encode the pipeline
# distinction in the label rather than dumping a second diagnostic.
WHISPER_DEMUCS_PROVIDER_MARKER = "[# Generated Lyrics with Demucs + Whisper]"

# Provider name emitted by :class:`FasterWhisperAnalyzer`.  Centralised
# here so the GUI / CLI / LRC writers don't drift if the analyzer is
# ever renamed -- one constant change keeps the marker logic in sync.
WHISPER_PROVIDER_NAME = "whisper"


def _extract_name(field_val, default: str = "") -> str:
    """Coerce ``Song.primary_artist`` / ``Song.album`` into a plain name.

    ``lyricsgenius >= 3`` stores nested Genius entities (``primary_artist``,
    ``album``, etc.) as raw dicts carved straight from the JSON API response --
    not as ``Artist``/``Album`` instances.  Older fixtures, mocked responses, or
    future versions may return object instances with a ``.name`` attribute.
    This helper accepts both shapes (and ``None``/``str``) and returns the
    human-readable name.
    """
    if not field_val:
        return default
    if isinstance(field_val, str):
        return field_val or default
    if isinstance(field_val, dict):
        name = field_val.get("name")
        return name if isinstance(name, str) and name else default
    # Object-like (custom class with a ``name`` attribute)
    name = getattr(field_val, "name", None)
    if isinstance(name, str) and name:
        return name
    return default


def _format_diagnostics(result: "LyricsResult") -> list[str]:
    """Render a ``LyricsResult``'s provenance as a single ``[# ...]`` line.

    Output format::

        [# generated by <provider>; <sorted-key>=<value>; ...]

    When ``raw_extras`` is empty we still emit a provider-only line so the
    LRC carries a consistent provenance marker for every successful result
    (LRCLIB / Genius / heuristic / whisper / vad-preflight).

    Non-scalar extras (``None``, ``list``, ``dict``, ``set``, ``tuple``,
    ``Decimal``, ``UUID``, ``Path``, ``bytes``, ``datetime``, ...) and scalar
    values whose string form is empty or contains ``;`` or ``]`` are dropped
    with a ``LOG.warning``.  The format has no quoting, so a stray separator
    would corrupt the single ``[# ...]`` line and break the parser-safety
    guarantee below.

    The ``provider`` name is filtered the same way: empty, or ``;``/``]``,
    cause the entire diagnostic line to be suppressed (without a parsable
    provider there's nothing safe to record).

    Provider authors should keep ``raw_extras`` keys lowercase so the
    sorted-by-key iteration produces stable diffs across runs.    Conventional LRC parsers ``split_synced_text`` and ``header_lines``
    already skip ``[# ...]`` lines (their regexes demand ``[mm:ss]``
    timestamps or ``[a-zA-Z]+:..]`` headers respectively), so the
    diagnostic never bleeds into the visible lyric stream.
    """
    provider = result.provider or ""
    if not provider or ";" in provider or "]" in provider:
        LOG.warning(
            "diagnostic: dropping entire line; empty or unsafe provider name %r",
            provider,
        )
        return []
    # Whisper diagnostics are intentionally curtailed here: the human-
    # readable ``WHISPER_PROVIDER_MARKER`` (or its ``Demucs +`` variant)
    # already tells the reader where the synth lyrics came from, and the
    # verbose ``compute_type``/``device``/``duration``/... line is too
    # noisy for a file the user opens in their music player. ``raw_extras``
    # itself stays populated so non-LRC consumers (logger, GUI status)
    # keep the underlying detail. Other providers (LRCLIB, Genius,
    # heuristic, vad-preflight) keep the original verbose line because
    # they have no equivalent friendly marker.
    if provider == WHISPER_PROVIDER_NAME:
        return []
    extras: list[str] = [] 
    for key, value in sorted(result.raw_extras.items()):
        if not isinstance(value, (str, int, float, bool)):
            LOG.warning(
                "diagnostic: dropping non-scalar extra %s=%r",
                key, value,
            )
            continue
        rendered = str(value)
        if not rendered or ";" in rendered or "]" in rendered:
            LOG.warning(
                "diagnostic: dropping empty or unsafe extra %s=%r",
                key, value,
            )
            continue
        extras.append(f"{key}={rendered}")
    if extras:
        return [f"[# generated by {provider}; " + "; ".join(extras) + "]"]
    return [f"[# generated by {provider}]"]


@dataclass
class LyricsResult:
    """Successful response from a provider.

    ``synced_lines`` is the list of (timestamp_str, text) pairs; empty if the
    result is unsynced (e.g. from Genius).  ``source`` records which provider
    produced the data for later logging.
    """

    provider: str
    title: str
    artist: str
    album: str = ""
    length_seconds: float = 0.0
    synced_lines: list[tuple[str, str]] = field(default_factory=list)
    plain_text: str = ""
    instrumental: bool = False
    raw_extras: dict = field(default_factory=dict)

    @property
    def is_synced(self) -> bool:
        return bool(self.synced_lines)

    def to_document(self) -> LRCDocument:
        """Convert this result into an :class:`LRCDocument` ready for writing."""
        diagnostics: list[str] = _format_diagnostics(self)
        # When this result came from local faster-whisper transcription,
        # prepend a stable, human-readable marker so anyone reading the
        # .lrc file can tell at a glance that the lyrics are synthetic
        # and may need a manual pass for accuracy.  When the run also
        # went through Demucs vocal isolation, the label spells that out
        # so the reader knows the pipeline ordered differently than a
        # plain Whisper pass.  ``vocal_isolation`` is set by
        # :class:`FasterWhisperAnalyzer` to ``"demucs"`` or ``"raw"``;
        # any future value (or a missing key) falls back to the plain
        # Whisper marker rather than guessing.  The marker is the only
        # diagnostic line for Whisper -- ``_format_diagnostics`` no
        # longer emits the structured ``[# generated by whisper; ...]``
        # line, which kept ``compute_type``/``device``/``duration``/... in
        # the user's .lrc file but added nothing actionable for them.
        if self.provider == WHISPER_PROVIDER_NAME:
            if self.raw_extras.get("vocal_isolation") == "demucs":
                diagnostics.insert(0, WHISPER_DEMUCS_PROVIDER_MARKER)
            else:
                diagnostics.insert(0, WHISPER_PROVIDER_MARKER)
        doc = LRCDocument(
            title=self.title,
            artist=self.artist,
            album=self.album,
            length_seconds=self.length_seconds,
            diagnostic_lines=diagnostics,
        )
        if self.synced_lines:
            doc.lines = list(self.synced_lines)
        elif self.plain_text:
            for line in self.plain_text.splitlines():
                doc.add("", line)
        return doc


@dataclass
class LyricsFailure:
    provider: str
    reason: str


