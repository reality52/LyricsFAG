class LyricsFetcher:
    """Source-aware fetcher.  ``source`` is one of:
        ``"auto"`` (LRCLIB then Genius, then audio if enabled),
        ``"lrclib"`` / ``"genius"`` (only that provider for the
        LRCLIB/Genius stage, but the audio fallback still applies
        when the analyzer is enabled),
        ``"audio"`` (local Whisper+Demucs only; requires the audio
        analyzer to be enabled via ``--use-audio-analysis`` or the
        GUI's 'Use audio analysis' checkbox).
    """

    def __init__(
        self,
        *,
        source: str = "auto",
        genius_token: Optional[str] = None,
        session: Optional[requests.Session] = None,
        audio_analyzer: Optional["object"] = None,
        cancel_event: Optional["threading.Event"] = None,
    ) -> None:
        """Construct a fetcher.

        ``audio_analyzer`` is an optional third-tier fallback (e.g.
        ``FasterWhisperAnalyzer``).  When provided, ``source="auto"`` chains
        ``lrclib -> genius -> audio_analyzer``.  ``cancel_event`` is checked
        between segments for prompt GUI cancellation.
        """
        self.source = source
        self.lrclib = LRCLIBClient(session=session)
        self.genius = GeniusClient(access_token=genius_token)
        self.audio_analyzer = audio_analyzer
        self.cancel_event = cancel_event

    # Substrings in the *filename* (case-insensitive, punctuation/whitespace
    # normalised) that mark a track as an instrumental / karaoke / backing-
    # track version.  We deliberately only short-circuit the LOCAL audio
    # analysis branch on a match -- LRCLIB + Genius still get a chance to
    # return the original song's lyrics, since ``"Song (Off Vocal).flac"``
    # is often a faithful cover of a real song that LRCLIB knows.
    # Filename check only; a title-tag counterpart is not implemented.
    _INSTRUMENTAL_FILENAME_PATTERNS: tuple[str, ...] = (
        # English patterns (existed since v1.1.0).
        "backing track",
        "inst version",
        "instrumental",
        "instrumental version",
        "karaoke",
        "minus one",
        "no vocal",
        "no vocals",
        "off vocal",        
        "vocal removed",
        "vocals removed",
        # No-separator / CamelCase variants. The CamelCase split in
        # _filename_blocks_audio (inserts a space at lowercase-uppercase
        # boundaries BEFORE lowercasing) already makes "SongOffVocal"
        # match the existing "off vocal" pattern. These dedicated
        # no-space entries are belt-and-suspenders for all-lowercase
        # filenames like "songoffvocal" where the CamelCase split
        # doesn't fire.
        "offvocal",
        "novocal",
        # Explicit hyphen variant. Redundant with "no vocal" after
        # the whitespace/hyphen normalisation, but kept per user
        # request for grep-ability.
        "no-vocal",
        # SEMANTIC NOTE: "acapella" / "акапелла" literally mean
        # "vocal-only, no instruments" -- the OPPOSITE of instrumental.
        # Whisper CAN transcribe these (there are vocals to transcribe).
        # Added per user request; remove these two entries if skipping
        # Whisper on a cappella files turns out to be undesirable.
        "acapella",
        "акапелла",
        # Russian equivalents of the English patterns above. The same
        # normalisation (CamelCase split + whitespace/punctuation
        # collapse) catches the usual variants.
        "инструментал",
        "караоке",
        "минусовка",
        "без вокала",
    )
    # Compiled once: a single anchored substring search is cheaper than 11
    # separate ``in`` checks on every audio file in a 300-track batch. The
    # outer ``(?:^|[\\s\\-_.])`` / ``(?:$|[\\s\\-_.])`` pair means a match
    # has to start AND end at a word/punctuation boundary -- ``"instrument"``
    # doesn't trigger ``"instrumental"``, and ``"tracklist"`` doesn't trigger
    # ``"backing track"``.
    _INSTRUMENTAL_FILENAME_RE = re.compile(
        r"(?:^|[\s\-_.])("
        + "|".join(re.escape(p) for p in _INSTRUMENTAL_FILENAME_PATTERNS)
        + r")(?:$|[\s\-_.])",
        re.IGNORECASE,
    )

    @staticmethod
    def _filename_blocks_audio(audio: AudioFile) -> bool:
        """True if ``audio.path`` looks like an instrumental/karaoke version.

        Normalises the stem in TWO steps so the usual variants all match
        the same pattern:

        1. **CamelCase split** -- inserts a space at every
           lowercase-uppercase boundary (``offVocal`` -> ``off Vocal``,
           ``SongOffVocal`` -> ``Song Off Vocal``). This MUST run before
           lowercasing, because lowercasing first would lose the
           boundary positions.
        2. **Whitespace / punctuation collapse + lowercase** -- collapses
           any run of whitespace / ``-`` / ``_`` / ``.`` / ``\\`` to a
           single space, then lowercases. So ``"Song-Off-Vocal.flac"``,
           ``"song_off_vocal.flac"`` and ``"song off vocal.flac"`` all
           normalise to ``"song off vocal"`` and match the existing
           ``"off vocal"`` pattern.

        Used to short-circuit the audio-analysis branch in :meth:`fetch`;
        the LRCLIB / Genius branches are untouched so a karaoke file
        whose original has real lyrics still gets an LRC.
        """
        # Unicode-aware CamelCase split: matches at every
        # lowercase-then-uppercase boundary for BOTH ASCII and
        # Cyrillic. The Cyrillic ranges are U+0430–U+044F (lowercase)
        # and U+0410–U+042F (uppercase), which covers standard
        # Russian filenames like ``ПесняИнструментал``. Without
        # these ranges, the Cyrillic boundary is invisible to the
        # ASCII-only ``[a-z][A-Z]`` pattern, and ``ПесняИнструментал``
        # never gets split -- so the new ``инструментал`` pattern
        # can't match (the ASCII-only ``[\s\-_.]`` boundary in the
        # compiled regex would see a Cyrillic letter, not a boundary,
        # at the position between ``я`` and ``и``).
        stem_with_camelcase_spaces = re.sub(
            r"([a-z\u0430-\u044f])([A-Z\u0410-\u042f])", r"\1 \2",
            audio.path.stem,
        )
        name = re.sub(
            r"[\s\-_.]+", " ", stem_with_camelcase_spaces.lower()
        ).strip()
        return bool(LyricsFetcher._INSTRUMENTAL_FILENAME_RE.search(name))

    def fetch(self, audio: AudioFile) -> LyricsResult | LyricsFailure:
        # Instrumental short-circuit (filename only) lives in the ``audio``
        # branch below.  LRCLIB + Genius are deliberately NOT gated -- a
        # karaoke file whose original song has real lyrics still gets an LRC.

        if self.source == "auto":
            order = ["lrclib", "genius"]
            if self.audio_analyzer is not None:
                order.append("audio")
        elif self.source in ("lrclib", "genius"):
            order = [self.source] + (
                ["audio"] if self.audio_analyzer is not None else []
            )
        elif self.source == "audio":
            # Source='audio' is the local-only chain: a missing or
            # disabled audio analyzer (CLI user forgot
            # ``--use-audio-analysis``, OR faster-whisper failed to
            # construct / isn't installed) would otherwise leave the
            # order list empty and trip the ``assert last is not None``
            # below -- crashing the worker mid-batch. Short-circuit to
            # a clean LyricsFailure so the run finishes normally with
            # a ``missing`` outcome (the per-file line in the log
            # panel / summary explains why).
            if self.audio_analyzer is None or not self.audio_analyzer.enabled:
                return LyricsFailure(
                    "audio",
                    "source='audio' requires --use-audio-analysis "
                    "(audio analyzer unavailable)",
                )
            order = ["audio"]
        else:
            return LyricsFailure("config", f"unknown source {self.source!r}")

        last: LyricsResult | LyricsFailure | None = None
        for provider in order:
            if provider == "lrclib":
                r = self.lrclib.get(
                    artist=audio.artist,
                    title=audio.title,
                    album=audio.album,
                    duration=audio.duration,
                )
            elif provider == "genius":
                if not self.genius.enabled:
                    last = LyricsFailure("genius", "no token / not installed")
                    continue
                r = self.genius.get(artist=audio.artist, title=audio.title)
            elif provider == "audio":
                if self.audio_analyzer is None or not self.audio_analyzer.enabled:
                    last = LyricsFailure(
                        self.audio_analyzer.name if self.audio_analyzer else "audio",
                        "audio analyzer disabled / not installed",
                    )
                    continue
                # Filename-based short-circuit: never run Whisper on a
                # track that the user clearly named as instrumental /
                # karaoke / off-vocal. The LRCLIB + Genius branches
                # above are NOT gated by this check on purpose -- those
                # providers index the original song and often return
                # usable lyrics even when the local file is a cover /
                # backing track.
                if self._filename_blocks_audio(audio):
                    last = LyricsFailure(
                        "audio",
                        "filename matches karaoke/instrumental pattern; "
                        "audio analysis skipped",
                    )
                    LOG.info(
                        "Skipping audio analysis for %s: filename matches "
                        "karaoke/instrumental pattern",
                        audio.path.name,
                    )
                    continue
                r = self.audio_analyzer.get(audio, stop_event=self.cancel_event)
            else:
                continue  # pragma: no cover

            if isinstance(r, LyricsResult):
                # Accept synced lines OR a plain text body long enough to be useful.
                if r.is_synced or len(r.plain_text) >= 8:
                    return r
                # Provider returned a tiny plain snippet -- try next.
            last = r

        assert last is not None  # noqa: S101 - order is always non-empty
        return last


