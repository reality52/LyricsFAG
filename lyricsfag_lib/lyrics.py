"""Lyrics fetchers with a chained fallback strategy.

Defaults to LRCLIB first (returns synced lyrics natively -> best LRC output)
and falls back to Genius (huge database, unsynced plain text).  Either
provider can be selected exclusively via ``LyricsFetcher(source="...")``.

Network errors and empty results are tolerated; both providers log their
decisions so the caller can surface a "skipped: <reason>" message.
"""

